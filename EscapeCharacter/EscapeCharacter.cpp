#include <iostream>

using namespace std;

int main() {
    cout << "Hello, World!\n";
    cout << "This is a line with a tab:\tTabbed text.\n";
    cout << "This is a line with a backslash: \\\n";
    cout << "This is a line with a double quote: \"Quoted text\"\n";
    cout << "This is a line with a single quote: \'Single quoted text\'\n";
    cout << "This is a line with a newline character:\nNew line starts here.\n";
    return 0;
}